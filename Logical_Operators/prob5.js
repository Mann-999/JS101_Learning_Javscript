
let today = "sat";

switch(today){
  case "sun" : console.log("Sunday");
    break;
  case "sat" : console.log("Saturday");
    break;
  case "fri" : console.log("Friday");
    break;
  case "thur" : console.log("Thursday");
    break;
  case "wed" : console.log("Wednesday");
    break;  
  case "tue" : console.log("Tuesday");
    break;
  case "mon" : console.log("Monday");
}